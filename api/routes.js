/**
 * GET /api/burnout
 * Returns burnout scores sorted red → yellow → green (highest risk first).
 */
router.get('/burnout', (req, res) => {
  try {
    const sorted = [...store.burnoutScores].sort(
      (a, b) =>
        (BURNOUT_ORDER[a.burnoutRisk] ?? 3) - (BURNOUT_ORDER[b.burnoutRisk] ?? 3)
    );
    res.json(sorted);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

