/**
 * POST /api/auth/login
 * Body: { loginId, password }
 * Simple credential check — returns user record (minus password) on success.
 * NOTE: No JWT/sessions here — demo only. Add proper auth middleware in production.
 */
router.post('/auth/login', (req, res) => {
  try {
    const { loginId, password } = req.body;
    if (!loginId || !password) {
      return res.status(400).json({ error: 'loginId and password are required.' });
    }

    const user = store.users.find((u) => u.loginId === loginId);
    if (!user || user.password !== password) {
      return res.status(401).json({ error: 'Invalid credentials.' });
    }

    // Return user info without the password field
    const { password: _pw, ...safeUser } = user;
    res.json({ message: 'Login successful.', user: safeUser });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

/**
 * GET /api/users
 * Returns all user accounts (passwords omitted).
 */
router.get('/users', (_req, res) => {
  try {
    const safeUsers = store.users.map(({ password: _pw, ...u }) => u);
    res.json(safeUsers);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

