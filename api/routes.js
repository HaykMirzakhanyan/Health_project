/**
 * GET /api/interventions/pending
 * Returns only interventions that have NOT yet been approved, sorted by urgency.
 */
router.get('/interventions/pending', (req, res) => {
  try {
    const pending = store.interventions
      .filter((i) => !i.approved)
      .sort((a, b) => (URGENCY_ORDER[a.urgency] ?? 4) - (URGENCY_ORDER[b.urgency] ?? 4));
    res.json(pending);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

/**
 * POST /api/interventions/:id/approve
 * Marks an intervention as approved (charge nurse one-click approval).
 * In production this would also trigger the downstream scheduling action.
 */
router.post('/interventions/:id/approve', (req, res) => {
  try {
    const intervention = store.interventions.find((i) => i.id === req.params.id);
    if (!intervention) {
      return res.status(404).json({ error: `Intervention ${req.params.id} not found.` });
    }
    if (intervention.approved) {
      return res.status(400).json({ error: 'Intervention has already been approved.' });
    }

    intervention.approved = true;
    res.json({ message: 'Intervention approved.', intervention });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

