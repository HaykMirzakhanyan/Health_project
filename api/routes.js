/**
 * api/routes.js
 * Express Router — all REST endpoints for the Health Staff Scheduler.
 *
 * All routes follow the same error convention:
 *   success → 200 (or 201) with JSON body
 *   not found → 404 { error: "..." }
 *   bad request → 400 { error: "..." }
 *   server error → 500 { error: "..." }
 */

'use strict';

const express = require('express');
const router = express.Router();

const { store } = require('../data/schema');
const { runOrchestrator } = require('../agents/orchestrator');

// ---------------------------------------------------------------------------
// Urgency sort order helper (critical → high → medium → low)
// ---------------------------------------------------------------------------
const URGENCY_ORDER = { critical: 0, high: 1, medium: 2, low: 3 };
const BURNOUT_ORDER = { red: 0, yellow: 1, green: 2 };

// ===========================================================================
// Health check
// ===========================================================================

/**
 * GET /api/health
 * Simple liveness check. Returns the timestamp of the last orchestrator run.
 */
router.get('/health', (req, res) => {
  res.json({ status: 'ok', lastRun: store.lastRun });
});

// ===========================================================================
// Orchestrator
// ===========================================================================

/**
 * POST /api/orchestrator/run
 * Triggers the full 4-agent pipeline and returns the digest.
 * This is the primary demo endpoint — expect it to take 10–30 seconds.
 */
router.post('/orchestrator/run', async (req, res) => {
  try {
    const digest = await runOrchestrator();
    res.json(digest);
  } catch (err) {
    console.error('[POST /orchestrator/run]', err);
    res.status(500).json({ error: err.message });
  }
});

