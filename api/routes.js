/**
 * GET /api/appointments
 * Returns all appointments for today.
 * Optional query params: ?unit=ICU-1  ?status=scheduled  ?staffId=<id>
 */
router.get('/appointments', (req, res) => {
  try {
    let appts = store.appointments;
    if (req.query.unit)    appts = appts.filter((a) => a.unit === req.query.unit);
    if (req.query.status)  appts = appts.filter((a) => a.status === req.query.status);
    if (req.query.staffId) appts = appts.filter((a) => a.assignedStaffId === req.query.staffId);
    res.json(appts);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

/**
 * PATCH /api/appointments/:id/status
 * Body: { status: 'completed' | 'in_progress' | 'cancelled' }
 * Updates the status of an appointment.
 */
router.patch('/appointments/:id/status', (req, res) => {
  try {
    const appt = store.appointments.find((a) => a.id === req.params.id);
    if (!appt) {
      return res.status(404).json({ error: `Appointment ${req.params.id} not found.` });
    }
    const validStatuses = ['scheduled', 'in_progress', 'completed', 'cancelled'];
    const { status } = req.body;
    if (!validStatuses.includes(status)) {
      return res.status(400).json({ error: `status must be one of: ${validStatuses.join(', ')}` });
    }
    appt.status = status;
    res.json({ message: 'Appointment status updated.', appointment: appt });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

