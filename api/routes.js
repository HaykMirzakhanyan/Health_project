/**
 * POST /api/checkin/:staffId
 * Body: { wellnessScore: 1-5 }
 * Updates a staff member's self-reported wellness score.
 * In production, this feeds the burnout model in real time via a Firestore
 * trigger or a queued background job.
 */
router.post('/checkin/:staffId', (req, res) => {
  try {
    const member = store.staff.find((s) => s.id === req.params.staffId);
    if (!member) {
      return res.status(404).json({ error: `Staff member ${req.params.staffId} not found.` });
    }

    const { wellnessScore } = req.body;
    if (wellnessScore === undefined || wellnessScore === null) {
      return res.status(400).json({ error: 'wellnessScore is required in the request body.' });
    }

    const score = Number(wellnessScore);
    if (!Number.isInteger(score) || score < 1 || score > 5) {
      return res
        .status(400)
        .json({ error: 'wellnessScore must be an integer between 1 and 5.' });
    }

    member.wellnessScore = score;

    console.log(`[Checkin] ${member.name} submitted wellnessScore: ${score}`);
    res.json({ message: 'Wellness check-in recorded.', staff: member });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

